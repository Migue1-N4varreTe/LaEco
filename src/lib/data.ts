// Función para verificar si la tienda está abierta
export const isStoreOpen = (): boolean => {
  const now = new Date();
  const currentHour = now.getHours();
  const openHour = parseInt(storeHours.open.split(":")[0]);
  const closeHour = parseInt(storeHours.close.split(":")[0]);

  return currentHour >= openHour && currentHour < closeHour;
};

// Función para obtener el próximo horario de apertura
export const getNextOpenTime = (): string => {
  const now = new Date();
  const tomorrow = new Date(now);
  tomorrow.setDate(tomorrow.getDate() + 1);

  if (now.getHours() >= parseInt(storeHours.close.split(":")[0])) {
    return `Mañana a las ${storeHours.open}`;
  } else {
    return `Hoy a las ${storeHours.open}`;
  }
};

